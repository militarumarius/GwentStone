package game;

public class Coordinates {
    private int x;
    private int y;

    /**
     */
    public int getX() {
        return x;
    }

    /**
     */
    public int getY() {
        return y;
    }


    public Coordinates(final int x, final int y) {
        this.x = x;
        this.y = y;
    }
}
